public class Persona {
	
	protected String nTelefono;
	
	// Constructor
	public Persona(String nTelefono) {	
		this.nTelefono = nTelefono;
	}
	//Getter y setter
	public String getNumeroDeTelefono() {
		return nTelefono;
	}
	public void setNumeroDeTelefono(String nTelefono) {
		this.nTelefono = nTelefono;
	}
}
